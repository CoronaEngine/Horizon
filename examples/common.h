inline std::string readStringFile(const std::string_view filePath)
{
    std::ifstream file{std::string(filePath)};
    if (!file.is_open())
    {
        throw std::runtime_error("Could not open file: " + std::string(filePath));
    }

    std::stringstream buffer;
    buffer << file.rdbuf();
    return buffer.str();
}

